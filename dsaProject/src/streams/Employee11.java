package streams;

public class Employee11 {

	private String name;
	private String department;
	private double salary;

	public Employee11(String name, String department, double salary) {
		this.name = name;
		this.department = department;
		this.salary = salary;
	}

	public String getName() {
		return name;
	}

	public String getDepartment() {
		return department;
	}

	public double getSalary() {
		return salary;
	}

}
